class FrenchColorAdapter < ColorizedString
  def noir
    black
  end

  def noir_clair
    light_black
  end

  def rouge
    red
  end

  def rouge_clair
    light_red
  end

  def vert
    green
  end

  def vert_clair
    light_green
  end

  def jaune
    yellow
  end

  def jaune_clair
    light_yello
  end

  def bleu
    blue
  end

  def bleu_clair
    light_blue
  end

  def blanc
    white
  end
end

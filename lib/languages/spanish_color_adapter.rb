class SpanishColorAdapter < ColorizedString
  def negro
    black
  end

  def negro_claro
    light_black
  end

  def rojo
    red
  end

  def rojo_claro
    light_red
  end

  def verde
    green
  end

  def verde_claro
    light_green
  end

  def amarillo
    yellow
  end

  def amarillo_claro
    light_yellow
  end

  def azul
    blue
  end

  def azul_claro
    light_blue
  end

  def blanco
    white
  end
end
